CREATE TABLE IF NOT EXISTS "Chantiers" (
	"IDChantier"	INTEGER NOT NULL UNIQUE,
	"NomChantier"	TEXT,
	"IDProjet"	INTEGER NOT NULL,
	"IDDepartement"	INTEGER NOT NULL,
