CREATE TABLE IF NOT EXISTS "Departement" (
	"IDDepartement"	INTEGER NOT NULL,
	"NomDep"	TEXT NOT NULL,
	"Directeur"	TEXT NOT NULL,
	PRIMARY KEY("IDDepartement" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "Heures_Travaillées" (
	"IDProjet"	INTEGER,
	"IDEmploye"	INTEGER,
	"NbrHT"	INTEGER,
	PRIMARY KEY("IDProjet","IDEmploye"),
	FOREIGN KEY("IDEmploye") REFERENCES "Employes"("IDEmploye"),
	FOREIGN KEY("IDProjet") REFERENCES "Projets"("IDProjet")
);
CREATE TABLE IF NOT EXISTS "Employes" (
	"IDEmploye"	INTEGER,
	"nom"	TEXT NOT NULL,
	"adresseMail"	TEXT NOT NULL,
	"IDCsp"	INTEGER NOT NULL,
	"IDDepartement"	INTEGER NOT NULL,
	PRIMARY KEY("IDEmploye" AUTOINCREMENT),
	FOREIGN KEY("IDDepartement") REFERENCES "Departement"("IDDepartement"),
	FOREIGN KEY("IDCsp") REFERENCES "CSP"("IDCsp")
);
CREATE TABLE IF NOT EXISTS "Service_Technique" (
	"IDProjet"	INTEGER,
	"IDCsp"	INTEGER,
	"NbrHE"	INTEGER,
	PRIMARY KEY("IDProjet","IDCsp"),
	FOREIGN KEY("IDCsp") REFERENCES "CSP"("IDCsp"),
	FOREIGN KEY("IDProjet") REFERENCES "Projets"("IDProjet")
);
